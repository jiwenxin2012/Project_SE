package com.example.first;
import java.util.ArrayList;  
import java.util.HashMap;  
  
import android.content.Context;  
import android.database.Cursor;
import android.graphics.Color;
import android.view.LayoutInflater;  
import android.view.View;  
import android.view.ViewGroup;  
import android.widget.BaseAdapter;  
import android.widget.CheckBox;  
import android.widget.TextView;  
  
public class MyListAdapter extends BaseAdapter {  
    // 填充数据的list  
    private Cursor list;  
    private ArrayList<Boolean> use;  
    // 用来控制CheckBox的选中状况  
    private static HashMap<Integer, Boolean> isSelected;  
    // 上下文  
    private Context context;  
    // 用来导入布局  
    private LayoutInflater inflater = null;  
    // 构造器  
    public MyListAdapter(Cursor list, Context context, ArrayList<Boolean> use) {  
        this.context = context;  
        this.list = list;  
        this.use = use;
        inflater = LayoutInflater.from(context);  
        isSelected = new HashMap<Integer, Boolean>();  
        // 初始化数据  
        initDate();  
    }  
  
    // 初始化isSelected的数据  
    private void initDate() {  
        for (int i = 0; i < list.getCount(); i++) {  
        	list.moveToPosition(i);
            if (use.get(i)) {
            	getIsSelected().put(i, true); 
            }
            else {
            	getIsSelected().put(i, false);  
            }
        }  
    }  
  
    @Override  
    public int getCount() {  
        return list.getCount();  
    }  
  
    @Override  
    public Object getItem(int position) {  
    	if (list.moveToPosition(position)) {  
            return list;  
        } else {  
            return null;  
        }  
    }  
  
    @Override  
    public long getItemId(int position) {  
        return position;  
    }  
  
    @Override  
    public View getView(int position, View convertView, ViewGroup parent) {  
        ViewHolder holder = null;  
        if (convertView == null) {  
            // 获得ViewHolder对象  
            holder = new ViewHolder();  
            // 导入布局并赋值给convertview  
            convertView = inflater.inflate(R.layout.listviewitem, null);  
            holder.tv = (TextView) convertView.findViewById(R.id.item_tv);  
            holder.cb = (CheckBox) convertView.findViewById(R.id.item_cb);  
            // 为view设置标签  
            convertView.setTag(holder);  
        } else {  
            // 取出holder  
            holder = (ViewHolder) convertView.getTag();  
        }  
        // 设置list中TextView的显示  
        list.moveToPosition(position);
        holder.tv.setText(list.getString(list.getColumnIndex(NotesDbAdapter.KEY_NOTE)));  
        // 根据isSelected来设置checkbox的选中状况  
        holder.cb.setChecked(getIsSelected().get(position));  
        return convertView;  
    }  
  
    public static HashMap<Integer, Boolean> getIsSelected() {  
        return isSelected;  
    }  
  
    public static void setIsSelected(HashMap<Integer, Boolean> isSelected) {  
        MyListAdapter.isSelected = isSelected;  
    }  
  
    public static class ViewHolder {  
        TextView tv;  
        CheckBox cb;  
    }  
}  